package modelo.DTO;

/**
 *
 * @author edgarbeltran
 */
public class PersonaDTO {
    int dni;
    String nombre;
    String correo;
    String telefono;

    public PersonaDTO() {
    }

    public PersonaDTO(int dni, String nombre, String correo, String telefono) {
        this.dni = dni;
        this.nombre = nombre;
        this.correo = correo;
        this.telefono = telefono;
    }

    public int getDni() {
        return dni;
    }

    public void setDni(int dni) {
        this.dni = dni;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }
    
    
}